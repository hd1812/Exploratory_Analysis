####Plotting Systems in R

##The Base Plotting System
##Idea: Start with blank canvas and build up on it. Intuitive. Plot+Annotation
##Example: plot()

##The Lattive System -- based on lattice library
##Use one function call can specify many information
##Plot are created with a single function call(xyplot, bwplot, etc)
##Most useful for concitioring types of plot: how y changes with x across levels of z
##Good for putting many plots on a screen

##The ggplot2 System -- based on ggplot2 system
##Idea: split the difference between base and lattice in a number of ways
##Automatically deals with spacings, text, titles, but also allows annotation
##similar to lattice but more intuitive
##Default mode makes many choices
##Example: qplot
